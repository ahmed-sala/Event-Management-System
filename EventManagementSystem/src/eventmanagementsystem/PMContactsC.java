    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cID = jTextField1_CustomerID.getText();
        String pmID = jTextField1_ProjectManagerID.getText();
        String message = jTextField1_ProjectManagerMessage.getText();
        if (pmID.equals("") || message.equals("")) {
            JOptionPane.showMessageDialog(this, "Fill All Inputs");
        } else {
            int CID = Integer.parseInt(cID);
            int PMID = Integer.parseInt(pmID);
            try (
                     Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + Global.getDatabaseName(), Global.getDatabaseUsername(), Global.getDatabasePassword());) {
                if (conn != null) {
                    System.out.println("Database: Manager approving event");
                    String query = "UPDATE contact SET message='" + message + "' WHERE id = " + CID + " and contact_id = " + PMID + ";";
                    Statement stmt = conn.createStatement();
                    stmt.executeUpdate(query);
                    query = "commit";
                    stmt.executeUpdate(query);
                    this.dispose();
                } else {
                    System.out.println("Failed to make connection!");
                }
            } catch (SQLException e) {
                System.err.format("SQL State: %s\n%s", e.getSQLState(), e.getMessage());
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // Replace "username" and "password" with your MySQL username and password
        String url = "jdbc:mysql://localhost:3306/" + Global.getDatabaseName();
        String username = Global.getDatabaseUsername();
        String password = Global.getDatabasePassword();
        // Create a statement
        try ( // Connect to the database
                 Connection connection = DriverManager.getConnection(url, username, password)) {
            // Create a statement
            PreparedStatement ps;
            ps = connection.prepareStatement("SELECT * FROM contact");
            ResultSet resultSet = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rowCount = model.getRowCount();
            //Remove rows one by one from the end of the table
            for (int i = rowCount - 1; i >= 0; i--) {
                model.removeRow(i);
            }
            // Insert the data into the customer table
            Object[] row;
            while (resultSet.next()) {
                row = new Object[5];
                row[0] = resultSet.getInt(1);
                row[1] = resultSet.getString(2);
                row[2] = resultSet.getString(3);
                row[3] = resultSet.getString(4);
                row[4] = resultSet.getInt(5);
                model.addRow(row);
            }
            connection.close();
        } catch (SQLException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

